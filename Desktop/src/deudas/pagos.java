    private void btnGuardarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPagoActionPerformed
        // TODO add your handling code here:
        deudaModelo objDeudas = (deudaModelo) cmbDeudas.getSelectedItem();
        ingresosModelo objIngresos = (ingresosModelo) cmbIngresos.getSelectedItem();
        if (objDeudas.getIddeuda() == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una deuda", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (objIngresos.getIdingreso() == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un ingreso", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                List<ingresosModelo> objIngresoPorId = null;
                double ingresoActual = 0.0;
                try {
                    objIngresoPorId = new ingreso().consultaIngresosPorID(objIngresos.getIdingreso());
                    if (objIngresoPorId.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "No se puede tomar saldo de este ingreso", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        //Obtiene la cantidad del ingreso seleccionado
                        ingresoActual = objIngresoPorId.get(0).getCantidad_actual();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(pagos.class.getName()).log(Level.SEVERE, null, ex);
                }
                double abono = Double.parseDouble(txtAAbonoDinero.getText().trim());
                //Comparar si la cantidad actual es mayor o igual que el ingreso actual
                if (ingresoActual >= abono) {
                    try {
                        //Falta hacer que se descuente la cantidad del ingreso, y de detalle_deuda
                        String res = new abonos().insertaAbono(abono, objDeudas.getIddeuda(), objIngresos.getIdingreso(), ingresoActual);
                        
                        if ("ok".equals(res)) {
                            JOptionPane.showMessageDialog(this, "Se ha insertado el ingreso con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                            txtAAbonoDinero.setText("");
                            txtSeDebe.setText("");
                            //principal.txtPanelInformacion.setText("Hola");
                            principal.cargaDatosFooter();
                            //cargaCombo();
                            cargaComboIngresos();
                        } else {
                            JOptionPane.showMessageDialog(this, res, "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(pagos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "La cantidad que se desea abonar supera el limite actual del ingreso que es "+ingresoActual, "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Favor de capturar solo numeros", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGuardarPagoActionPerformed

    private void cmbDeudasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDeudasItemStateChanged
        // TODO add your handling code here:
        deudaModelo objDeudas = (deudaModelo) cmbDeudas.getSelectedItem();
        try {
            if (objDeudas.getIddeuda() == -1) {
                txtSeDebe.setText("");
            } else {
                double monto = new deuda().obtieneMonto(objDeudas.getIddeuda());
                txtSeDebe.setText(String.valueOf(monto));
            }
        } catch (SQLException ex) {
            Logger.getLogger(pagos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbDeudasItemStateChanged

