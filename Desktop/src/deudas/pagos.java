    public void cargaCombo() throws SQLException {
        cmbDeudas.removeAllItems();
        List<deudaModelo> lista = new deuda().consultaDeudaConAdeudo();
        deudaModelo dm1 = new deudaModelo("Selecciona una opcion", -1);
        cmbDeudas.addItem(dm1);
        if (lista == null || lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se han encontrado deudas activas aún");
            cmbDeudas.repaint();
        } else {
            for (deudaModelo dm : lista) {
                cmbDeudas.addItem(dm);
            }
            cmbDeudas.repaint();
        }
    }

    public void cargaComboIngresos() throws SQLException {
        cmbIngresos.removeAllItems();
        List<ingresosModelo> lista = new ingreso().consultaIngresos();
        ingresosModelo dm1 = new ingresosModelo("Selecciona una opcion", -1);
        cmbIngresos.addItem(dm1);
        if (lista == null || lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se han encontrado ingresos activos aún");
            cmbIngresos.repaint();
        } else {
            for (ingresosModelo dm : lista) {
                cmbIngresos.addItem(dm);
            }
            cmbIngresos.repaint();
        }
    }

