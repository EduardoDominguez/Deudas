     public void cargaCombo() throws SQLException {
        List<deudaModelo> lista = new deuda().consultaDeuda();
        cmbDeudas.removeAllItems();
        if (lista == null || lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se han encontrado deudas activas aún");
            cmbDeudas.repaint();
            this.setVisible(false);
        }else{
            deudaModelo dm1 = new deudaModelo();
            dm1.setIddeuda(-1);
            dm1.setNombre("Selecciona una deuda");
            cmbDeudas.addItem(dm1);
            lista.stream().forEach((dm) -> {
                cmbDeudas.addItem((deudaModelo) dm);
            });
            cmbDeudas.repaint();
        }
    }

