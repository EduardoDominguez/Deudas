    private void cmbDeudasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDeudasItemStateChanged
        // TODO add your handling code here:
        deudaModelo objDeudas = (deudaModelo) cmbDeudas.getSelectedItem();
        try {
            if (objDeudas.getIddeuda() == -1) {
                txtSeDebe.setText("");
            } else {
                double monto = new deuda().obtieneMonto(objDeudas.getIddeuda());
                txtSeDebe.setText(String.valueOf(monto));
            }
        } catch (SQLException ex) {
            Logger.getLogger(pagos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbDeudasItemStateChanged

