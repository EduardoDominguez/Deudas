    public void cargaDatos() throws SQLException {
        modeloDeudas = new DefaultTableModel(null, new String[]{"Nombre", "Dia de corte"," Dia limite de pago", "Estatus", "Fecha registro"," Cantidad pendiente"});
        List<deudaModelo> lista = new deuda().consultaDeudaConTotal();
        if (lista == null || lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se han encontrado deudas activas a√∫n", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            for (deudaModelo dm : lista) {
                Object [] arr = new Object [6];
                arr[0]= dm.getNombre();
                arr[1]= dm.getDia_corte();
                arr[2]= dm.getDia_limite_pago();
                //String estado= dm.getEstatus() = 1 ? estado = "ACTIVO" : "INACTIVO";
                arr[3]= dm.getEstatus();
                arr[4]= dm.getFechaalta();
                arr[5]= dm.getTotal();
                
                modeloDeudas.addRow(arr);
            }
        }
        //tablaDeudas.addRow();
    }

