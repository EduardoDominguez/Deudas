    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
       try {
            double cantidad = Double.parseDouble(txtCantidadDeuda.getText());
            System.out.println(cantidad);
            //String pattern = "MM/dd/yyyy";
            
            deudaModelo objDeuda= (deudaModelo) cmbDeudasDetalles.getSelectedItem();
            int iddeuda = objDeuda.getIddeuda();
            try {
                String res = new deuda().insertaDetalleDeuda(iddeuda,cantidad);
                if ("ok".equals(res)) {
                    JOptionPane.showMessageDialog(this, "Se ha insertado el ingreso con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    txtCantidadDeuda.setText("");
                    principal.cargaDatosFooter();
                } else {
                    JOptionPane.showMessageDialog(this, res, "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ingresos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese solo números en el campo de cantidad", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

