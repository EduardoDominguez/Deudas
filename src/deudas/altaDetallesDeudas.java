     public void cargaCombo() throws SQLException {
        List<deudaModelo> lista = new deuda().consultaDeudaComboDetalle();
        cmbDeudasDetalles.removeAllItems();
        if (lista == null || lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se han encontrado deudas a las cuales es necesario asignarles detalle.\n"
                    + "Si necesita  hacer un cargo a una deuda esta la opción Deudas fijas -> Hacer cargo a deuda");
            cmbDeudasDetalles.repaint();
            this.setVisible(false);
        }else{
            for(deudaModelo dm : lista){
                cmbDeudasDetalles.addItem((deudaModelo) dm);
                //cmbDeudas.putClientProperty("id", dm.getIddeuda());
                //cmbDeudas.insertItemAt(dm.getNombre(), dm.getIddeuda());
            }
            cmbDeudasDetalles.repaint();
        }
    }
     
